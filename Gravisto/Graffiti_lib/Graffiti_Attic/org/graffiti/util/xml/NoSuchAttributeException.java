package org.graffiti.util.xml;

/**
 * Thrown when an attempt to access an attribute on an {@link XmlElement} fails.
 * 
 * @author Harald Frankenberger
 */
public class NoSuchAttributeException extends RuntimeException {

    /**
     * 
     */
    private static final long serialVersionUID = 6999303746576167637L;

    /**
     * Creates a new no-such-attribute-exception with no detail message.
     * 
     */
    public NoSuchAttributeException() {
    }

    /**
     * Creates a new no-such-attribute-exception with the given message.
     * 
     * @param message
     *            the detail message
     */
    public NoSuchAttributeException(String message) {
        super(message);
    }

}

