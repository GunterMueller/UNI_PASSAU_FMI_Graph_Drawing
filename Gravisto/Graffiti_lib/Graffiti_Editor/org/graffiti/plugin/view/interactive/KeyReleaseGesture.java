package org.graffiti.plugin.view.interactive;

import java.awt.event.InputEvent;
import java.awt.event.KeyEvent;

/**
 * User gesture that represents a key release. It is provided for convenience
 * and its use by {@link InteractiveView}s is not prescribed.
 * 
 * @author Andreas Glei&szlig;ner
 * @version $Revision$ $Date$
 * @see UserGesture
 * @see InputEvent#getModifiersEx()
 * @see KeyEvent#getKeyCode()
 */
public class KeyReleaseGesture extends KeyboardGesture {
    /**
     * Constructs a {@code KeyReleaseGesture}.
     * 
     * @param keyCode
     *            the key code.
     * @param modifierMask
     *            a bit set representing the state of modal keys and mouse
     *            buttons.
     * @see InputEvent#getModifiersEx()
     */
    public KeyReleaseGesture(int keyCode, int modifierMask) {
        super(keyCode, modifierMask);
    }
}

