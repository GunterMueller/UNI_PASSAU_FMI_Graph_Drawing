package org.graffiti.plugins.ios.importers.ontology;

/**
 * Thrown when importing an ontology fails.
 * 
 * @author Harald
 * @version $Revision$ $Date$
 */
public class OntologyImportException extends RuntimeException {

    /**
     * 
     */
    private static final long serialVersionUID = 5586266581794002515L;

    /**
     * Creates an ontology-import-exception wrapping the specified exception.
     * 
     * @param e
     *            the exception to be wrapped.
     */
    public OntologyImportException(Exception e) {
        super(e);
    }

    /**
     * Creates an ontology-import-exception with no detail-message.
     */
    public OntologyImportException() {
    }

    /**
     * Creates an ontology-import-exception with the specified detail-message.
     * 
     * @param message
     *            the detail message of this exception.
     */
    public OntologyImportException(String message) {
        super(message);
    }

}
