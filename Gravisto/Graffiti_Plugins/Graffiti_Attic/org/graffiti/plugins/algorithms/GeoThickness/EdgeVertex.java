package org.graffiti.plugins.algorithms.GeoThickness;

/**
 * @author ma
 * 
 *         the objects, which are stored as Node in two-three-tree
 */

public class EdgeVertex {

    /** the edges becomes as locally object into this object stores */
    private LocalEdge edge;

    /** Constructor of the object */
    public EdgeVertex(LocalEdge edge) {
        this.edge = edge;
    }

    /** get the edges out this object */
    public LocalEdge getEdge() {
        return this.edge;
    }

    /** set the edges in this object */
    public void setEdge(LocalEdge edge) {
        this.edge = edge;
    }

}
