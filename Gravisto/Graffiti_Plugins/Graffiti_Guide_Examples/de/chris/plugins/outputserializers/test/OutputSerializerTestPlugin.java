package de.chris.plugins.outputserializers.test;

import org.graffiti.plugin.GenericPluginAdapter;
import org.graffiti.plugin.io.OutputSerializer;

/**
 * This is a simple example for an outputserializer plugin in Gravisto.
 */
public class OutputSerializerTestPlugin extends GenericPluginAdapter {

    /**
     * Creates a new OutputSerialzerTestPlugin object.
     */
    public OutputSerializerTestPlugin() {
        super();

        outputSerializers = new OutputSerializer[1];
        outputSerializers[0] = new TestOutputSerializer();
    }
}

