package quoggles.representation;

import javax.swing.JPanel;

/**
 * A direct subclass of <code>JPanel</code> that is used to represent a box
 * in the main panel of the system.
 */
public class BoxRepresentation extends JPanel {

    private IBoxRepresentation iBoxRepresentation;
    

    /**
     * 
     * @param iBoxRep
     */
    public BoxRepresentation(IBoxRepresentation iBoxRep) {
        super();
        iBoxRepresentation = iBoxRep;
    }


    /**
     * @param box
     */
    public void setIBoxRepresentation(IBoxRepresentation box) {
        iBoxRepresentation = box;
    }

    /**
     * @return
     */
    public IBoxRepresentation getIBoxRepresentation() {
        return iBoxRepresentation;
    }
}
