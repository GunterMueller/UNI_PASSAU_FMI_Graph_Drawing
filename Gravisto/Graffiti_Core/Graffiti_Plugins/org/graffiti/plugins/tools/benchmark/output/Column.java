package org.graffiti.plugins.tools.benchmark.output;

/**
 * @author Andreas Glei&szlig;ner
 * @version $Revision$ $Date$
 */
public class Column {
    private String item;
    private String name;

    public Column(String item) {
        this.item = item;
        this.name = item;
    }

    public String getItem() {
        return item;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }
}

