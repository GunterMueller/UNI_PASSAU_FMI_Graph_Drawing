package org.graffiti.plugins.scripting;

/**
 * Class implementing {@code ResultCallback} with empty methods. This class
 * exists as convenience for creating {@link ResultCallback} objects.
 * 
 * @author Andreas Glei&szlig;ner
 */
public class ResultCallbackAdapter implements ResultCallback {
    /**
     * {@inheritDoc}
     */
    public void reportError(String message) {
    }

    /**
     * {@inheritDoc}
     */
    public void reportResult(String value) {
    }

    /**
     * {@inheritDoc}
     */
    public void reportResult() {
    }
}
