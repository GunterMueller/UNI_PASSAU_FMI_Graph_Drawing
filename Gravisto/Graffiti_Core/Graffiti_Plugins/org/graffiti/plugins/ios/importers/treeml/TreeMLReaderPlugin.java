package org.graffiti.plugins.ios.importers.treeml;

import org.graffiti.plugin.GenericPluginAdapter;

/**
 * This plugin provides the functionality for reading TreeML files.
 * 
 * @author haeringp
 */
public class TreeMLReaderPlugin extends GenericPluginAdapter {

    /**
     * Constructs a new {@link TreeMLReaderPlugin}.
     */
    public TreeMLReaderPlugin() {
        super();
        this.inputSerializers = new TreeMLReader[1];
        this.inputSerializers[0] = new TreeMLReader();
    }
}

