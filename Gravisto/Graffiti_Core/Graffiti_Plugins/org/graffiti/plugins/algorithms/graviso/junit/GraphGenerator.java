    private static int[][] generateSquareGrid(int w) {
        int tot = w * w;
        int[][] g = new int[tot][tot];
        for (int i = 0; i < tot; i++) {
            for (int j = 0; j < tot; j++) {
                if (j == (i + 1) && (((j % w) != 0) || (i == 0))) {
                    g[i][j] = 1;
                    g[j][i] = 1;
                }
                if (j == (i + w)) {
                    g[i][j] = 1;
                    g[j][i] = 1;
                }
            }
        }
        return g;
    }

