package org.graffiti.plugins.algorithms.permutationgraph;

/**
 * @author Manu
 * @version $Revision$ $Date$
 */
public class NotALeafException extends Exception {

    /**
     * 
     */
    public NotALeafException() {
    }

    /**
     * @param message
     */
    public NotALeafException(String message) {
        super(message);
    }

    /**
     * @param cause
     */
    public NotALeafException(Throwable cause) {
        super(cause);
    }

    /**
     * @param message
     * @param cause
     */
    public NotALeafException(String message, Throwable cause) {
        super(message, cause);
    }

}

