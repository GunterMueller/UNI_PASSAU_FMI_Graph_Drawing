public class MinMaxPosition {
    // ConturElement position = new ConturElement();
    double xValue;
    double yValue;

    public MinMaxPosition() {

    }

    public MinMaxPosition(double xValue, double yValue) {
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public double getX() {
        return xValue;
    }

    public double getY() {
        return yValue;
    }

    @Override
    public String toString() {
        return xValue + " " + yValue;
    }
}

