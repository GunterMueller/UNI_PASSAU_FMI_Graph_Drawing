package org.graffiti.plugins.ios.importers.nexus;

/**
 * Contains the possible types a Token can have.
 */
public enum TokenTypes {
    LABEL,

    /** The end of a command. */
    COMMAND_DELIMITER,

    /** An assignment operator. */
    ASSIGNMENT,

    /** The start of a subtree in the Newick format. */
    SUBTREE_START,

    /** The end of a subtree in the Newick format. */
    SUBTREE_END,

    NEW_NODE_START,

    ATTRIBUTE_DELIMITER;
}
